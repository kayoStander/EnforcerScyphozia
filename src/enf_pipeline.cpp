  std::vector<char> Pipeline::ReadFile(const std::string &filepath) {
    std::ifstream file{ROOT + filepath, std::ios::ate | std::ios::binary};

    if (!file.is_open()) {
      throw std::runtime_error("Failed to open file: " + filepath);
    }

    size_t fileSize{static_cast<size_t>(file.tellg())};

    std::vector<char> buffer(fileSize);

    file.seekg(0);
    file.read(buffer.data(), static_cast<std::streamsize>(fileSize));

    file.close();
    return buffer;
  }

  void Pipeline::bind(VkCommandBuffer commandBuffer) {
    vkCmdBindPipeline(commandBuffer, VK_PIPELINE_BIND_POINT_GRAPHICS, graphicsPipeline);
  }

  void Pipeline::CreateGraphicsPipeline(const std::string &vertFilepath, const std::string &fragFilepath,
                                        const PipelineConfigInfo &configInfo,
                                        const std::vector<u32> specializedValues) {

    ASSERT_LOG(configInfo.pipelineLayout != VK_NULL_HANDLE,
               "Cannot create graphics pipeline :: no pipelineLayout provided");
    ASSERT_LOG(configInfo.renderPass != VK_NULL_HANDLE, "Cannot create graphics pipeline :: no renderPass provided");

    auto vertexCode{ReadFile(vertFilepath)};
    auto fragmentCode{ReadFile(fragFilepath)};

    LOG_INFO(Shader, "[Vertex:Fragment] => [{}:{}]", vertexCode.size(), fragmentCode.size());

    CreateShaderModule(vertexCode, &vertShaderModule);
    CreateShaderModule(fragmentCode, &fragShaderModule);

    std::vector<VkSpecializationMapEntry> entries{specializedValues.size()};
    for (u32 i{0}; i < specializedValues.size(); ++i) {
      entries[i].constantID = i;
      entries[i].offset = i * sizeof(u32);
      entries[i].size = sizeof(u32);
    }

    LOG_GENERIC_SPAN(Shader, specializedValues, "specializedValues => {}");

    VkSpecializationInfo specializationInfo{};
    specializationInfo.mapEntryCount = entries.size();
    specializationInfo.pMapEntries = entries.data();
    specializationInfo.dataSize = sizeof(u32) * specializedValues.size();
    specializationInfo.pData = specializedValues.data();

    // TODO: FOR CONSTANTS LIKE TEXTURE AMOUNT AND ETC
    // layout(constant_id = 0)const int mySpecialValue;
    // WARN: DONT FORGET YOU IDIOT

    VkPipelineShaderStageCreateInfo shaderStages[2]{};
    shaderStages[0].sType = VK_STRUCTURE_TYPE_PIPELINE_SHADER_STAGE_CREATE_INFO;
    shaderStages[0].stage = VK_SHADER_STAGE_VERTEX_BIT;
    shaderStages[0].module = vertShaderModule;
    shaderStages[0].pName = "main";
    shaderStages[0].flags = 0;
    shaderStages[0].pNext = nullptr;
    shaderStages[0].pSpecializationInfo = &specializationInfo;

    shaderStages[1].sType = VK_STRUCTURE_TYPE_PIPELINE_SHADER_STAGE_CREATE_INFO;
    shaderStages[1].stage = VK_SHADER_STAGE_FRAGMENT_BIT;
    shaderStages[1].module = fragShaderModule;
    shaderStages[1].pName = "main";
    shaderStages[1].flags = 0;
    shaderStages[1].pNext = nullptr;
    shaderStages[1].pSpecializationInfo = &specializationInfo;

    auto &bindingDescriptions = configInfo.bindingDescriptions;
    auto &attributeDescriptions = configInfo.attributeDescriptions;

    VkPipelineVertexInputStateCreateInfo vertexInputInfo{};
    vertexInputInfo.sType = VK_STRUCTURE_TYPE_PIPELINE_VERTEX_INPUT_STATE_CREATE_INFO;
    vertexInputInfo.vertexAttributeDescriptionCount = static_cast<u32>(attributeDescriptions.size());
    vertexInputInfo.vertexBindingDescriptionCount = static_cast<u32>(bindingDescriptions.size());
    vertexInputInfo.pVertexAttributeDescriptions = attributeDescriptions.data();
    vertexInputInfo.pVertexBindingDescriptions = bindingDescriptions.data();

    VkGraphicsPipelineCreateInfo pipelineInfo{};
    pipelineInfo.sType = VK_STRUCTURE_TYPE_GRAPHICS_PIPELINE_CREATE_INFO;
    pipelineInfo.stageCount = 2;
    pipelineInfo.pStages = shaderStages;
    pipelineInfo.pVertexInputState = &vertexInputInfo;
    pipelineInfo.pInputAssemblyState = &configInfo.inputAssemblyInfo;
    pipelineInfo.pViewportState = &configInfo.viewportInfo;
    pipelineInfo.pRasterizationState = &configInfo.rasterizationInfo;
    pipelineInfo.pMultisampleState = &configInfo.multisampleInfo;
    pipelineInfo.pColorBlendState = &configInfo.colorBlendInfo;
    pipelineInfo.pDepthStencilState = &configInfo.depthStencilInfo;
    pipelineInfo.pDynamicState = &configInfo.dynamicStateInfo;

    pipelineInfo.layout = configInfo.pipelineLayout;
    pipelineInfo.renderPass = configInfo.renderPass;
    pipelineInfo.subpass = configInfo.subpass;

    pipelineInfo.basePipelineIndex = -1;
    pipelineInfo.basePipelineHandle = VK_NULL_HANDLE;

    if (vkCreateGraphicsPipelines(device.device(), VK_NULL_HANDLE, 1, &pipelineInfo, nullptr, &graphicsPipeline) !=
        VK_SUCCESS) {
      throw std::runtime_error("Failed to create graphics pipeline");
    }
  }

  void Pipeline::CreateShaderModule(const std::vector<char> &code, VkShaderModule *shaderModule) {
    VkShaderModuleCreateInfo createInfo{};
    createInfo.sType = VK_STRUCTURE_TYPE_SHADER_MODULE_CREATE_INFO;
    createInfo.codeSize = code.size();
    createInfo.pCode = reinterpret_cast<const u32 *>(code.data());

    if (vkCreateShaderModule(device.device(), &createInfo, nullptr, shaderModule) != VK_SUCCESS) {
      throw std::runtime_error("Failed to create shader module");
    }
  }

