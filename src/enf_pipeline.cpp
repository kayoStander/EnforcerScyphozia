  Pipeline::Pipeline(Device &device, const std::string &vertFilepath, const std::string &fragFilepath,
                     const PipelineConfigInfo &configInfo, const std::vector<u32> specializedValues) : device{device} {
    CreateGraphicsPipeline(vertFilepath, fragFilepath, configInfo, specializedValues);
  }
  Pipeline::~Pipeline() noexcept {
    vkDestroyShaderModule(device.device(), vertShaderModule, nullptr);
    vkDestroyShaderModule(device.device(), fragShaderModule, nullptr);
    vkDestroyPipeline(device.device(), graphicsPipeline, nullptr);

    LOG_DEBUG(Vulkan, "Pipeline destroyed");
  }

